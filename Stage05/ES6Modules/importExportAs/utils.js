/*Import e Export as- Podemos importar e renomar os arquivos exportados de acordo
com o que gostariamos.
*/

const sum = (num , num2) => num + num2 


export {sum}


/*Import e Export * as- Iremos importar tudo do arquivo quando utilizamos o * e
daremos um nome a essa importação. Essa importação será como um objeto que conterá
métodos e propriedades.
*/


const mutl = (num , num2) => num * num2 
const sub = (num , num2) => num - num2 
const div = (num , num2) => num / num2 

export {mutl, sub , div}





